package generate

import (
	"strings"
	"testing"

	domainpkg "github.com/edelwud/terraci/plugins/github/internal/domain"
)

func TestToYAML(t *testing.T) {
	job, err := domainpkg.NewJob(domainpkg.JobOptions{
		RunsOn: "ubuntu-latest",
		Steps: []domainpkg.Step{
			domainpkg.NewStep(domainpkg.StepOptions{Name: "Checkout", Uses: "actions/checkout@v4"}),
			domainpkg.NewStep(domainpkg.StepOptions{Name: "Plan", Run: "terraform plan"}),
		},
	})
	if err != nil {
		t.Fatal(err)
	}
	builder := domainpkg.NewWorkflowBuilder(domainpkg.WorkflowOptions{
		Name: "Terraform",
		On: domainpkg.WorkflowTrigger{
			Push: &domainpkg.PushTrigger{Branches: []string{"main"}},
		},
		Env: map[string]string{"TF_IN_AUTOMATION": "true"},
	})
	addErr := builder.AddJob("plan-test", job)
	if addErr != nil {
		t.Fatalf("AddJob() error = %v", addErr)
	}
	workflow, err := builder.Build()
	if err != nil {
		t.Fatalf("Build() error = %v", err)
	}

	yamlBytes, err := workflow.ToYAML()
	if err != nil {
		t.Fatalf("ToYAML failed: %v", err)
	}

	yaml := string(yamlBytes)
	checks := []struct {
		desc     string
		contains string
	}{
		{"generated header", "Generated by terraci"},
		{"name field", "name:"},
		{"jobs field", "jobs:"},
		{"plan-test job", "plan-test:"},
		{"explicit user env", "TF_IN_AUTOMATION"},
	}
	for _, check := range checks {
		if !strings.Contains(yaml, check.contains) {
			t.Errorf("YAML should contain %s (%q)", check.desc, check.contains)
		}
	}
}
